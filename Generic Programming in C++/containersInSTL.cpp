// Containers in STL

// 1. Sequence Containers - implement data structures which can be accessed in a sequential manner
//      - vector
//      - list(Doubly linked list)
//      - deque (a queue that has two ends we can do insertion and deletion from both sides)
//      - arrays
//      - forward_list(singly linked list)

// 2. Container adaptors - provide a different interface for sequential containers
//      - Queue
//      - priority queue
//      - stack (can be build using array or vector but it only gives access to top most element)

// 3. Associative containers - implement sorted data structures that can be quickly searched (O(logN)Complexity)
//      - set
//      - multiset
//      - map
//      - multimap

// 4. Unordered Associative Containers - implement unordered data structures that can be quickly searched
//      - unordered set
//      - unordered multiset
//      - unordered map
//      - unordered multimap